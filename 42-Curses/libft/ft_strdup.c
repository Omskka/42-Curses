#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	size_t	i ;

	if (!s1)
	{
		return (NULL);
	}
	i = 0;
	str = (char *) malloc((sizeof (s1)-1) * 1);
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
